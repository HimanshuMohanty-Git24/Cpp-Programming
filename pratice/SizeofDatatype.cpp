#include<iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    cout<<"\nThe size of int data type is :"<<sizeof(int)<<" bytes";
    cout<<"\nThe size of float data type is :"<<sizeof(float)<<" bytes";
    cout<<"\nThe size of double data type is :"<<sizeof(double)<<" bytes";
    cout<<"\nThe size of char data type is :"<<sizeof(char)<<" bytes\n";
    return 0;
}
